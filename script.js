let total = 0;
// Basic Functions

// Add
function add(num) {
    total += num;
    return total;
}

// Subtract
function subtract(num) {
    total -= num;
    return total;
}

// Multiply
function multiply(num) {
    total *= num;
    return total;
}

// Divide
function divide(num) {
    total /= num;
    return total;
}